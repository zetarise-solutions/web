<script>
    // Services Contact Form
    document.getElementById('servicesContactForm').addEventListener('submit', function(e) {
        e.preventDefault();
        
        // Get selected services
        const selectedServices = [];
        document.querySelectorAll('.services-checkboxes input:checked').forEach(checkbox => {
            selectedServices.push(checkbox.nextElementSibling.textContent);
        });
        
        // Get form data
        const formData = new FormData(this);
        const data = Object.fromEntries(formData);
        data.services = selectedServices;
        
        // Simple validation
        if (!data.name || !data.email || !data.message) {
            alert('Please fill in all required fields.');
            return;
        }
        
        if (selectedServices.length === 0) {
            alert('Please select at least one service.');
            return;
        }
        
        // Simulate form submission
        alert('Thank you for your project request! Our team will contact you within 24 hours to discuss your requirements.');
        this.reset();
    });
</script>

